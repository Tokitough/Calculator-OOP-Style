# Calculator operations
class Calculator:
    def add(self, num_1, num_2):
        answer = num_1 + num_2
        return answer
    
    def subtract(self, num_1, num_2):
        answer = num_1 - num_2
        return answer
    
    def multiply(self, num_1, num_2):
        answer = num_1 * num_2
        return answer
    
    def divide(self, num_1, num_2):
        answer = num_1 / num_2
        return answer
    